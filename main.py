def main():

    for counter in range(10):

        character = Faker("ru_RU")

        generated_skills = random.sample(SKILLS, 3)
        beautiful_skills = []

        for skill in generated_skills:
            for letter in skill:
                skill = skill.replace(letter, RUNES[letter])
            beautiful_skills.append(skill)

        context = {
            "first_name": character.first_name(),
            "last_name": character.last_name(),
            "job": character.job(),
            "town": character.city(),
            "strength": random.randint(3, 18),
            "agility": random.randint(3, 18),
            "endurance": random.randint(3, 18),
            "intelligence": random.randint(3, 18),
            "luck": random.randint(3, 18),
            "skill_1": beautiful_skills[0],
            "skill_2": beautiful_skills[1],
            "skill_3": beautiful_skills[2]
        }

        result = os.path.join("res", "created{}.svg".format(counter+1))
        file_operations.render_template(INSERT, result, context)


if __name__ == "__main__":
    main()